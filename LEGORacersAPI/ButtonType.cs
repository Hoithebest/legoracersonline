namespace LEGORacersAPI
{
    /// <summary>
    /// Represents different button types.
    /// </summary>
    public enum ButtonType
    {
        /// <summary>
        /// A button with a checkmark icon.
        /// </summary>
        Checkmark = 0x46,
        /// <summary>
        /// A button with a cross icon.
        /// </summary>
        Cross = 0x45,
        /// <summary>
        /// A button with a right arrow icon.
        /// </summary>
        RightArrow = 0x44,
        /// <summary>
        /// A button with a left arrow icon.
        /// </summary>
        LeftArrow = 0x43,
        /// <summary>
        /// A button with no icon.
        /// </summary>
        NoIcon = 0x42
    }
}